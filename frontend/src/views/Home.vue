<script setup>
import { ref, computed, onMounted } from 'vue'
import { useRouter } from 'vue-router'
import HistoryDatabase from '../components/HistoryDatabase.vue'
import ScenarioPresets from '../components/ScenarioPresets.vue'
import { listScenarios, downloadScenarioFile } from '../api/scenarios'

const router = useRouter()
const scenarioPresetsRef = ref(null)

// formdata
const formData = ref({
  simulationRequirement: ''
})

// filelist
const files = ref([])

// handle scenario preset selection
const onScenarioLoaded = ({ files: scenarioFiles, simulationRequirement, scenarioName }) => {
  files.value = scenarioFiles
  formData.value.simulationRequirement = simulationRequirement
}

// auto-load a random scenario on mount
onMounted(async () => {
  try {
    const res = await listScenarios()
    const scenarios = res?.data?.scenarios
    if (scenarios && scenarios.length > 0) {
      const random = scenarios[Math.floor(Math.random() * scenarios.length)]
      // Download seed files
      const filePromises = (random.seed_files || []).map(f => downloadScenarioFile(random.id, f))
      const dlFiles = await Promise.all(filePromises)
      files.value = dlFiles
      formData.value.simulationRequirement = random.simulation_requirement || ''
    }
  } catch (e) {
    console.warn('Could not auto-load scenario:', e)
  }
})

// status
const loading = ref(false)
const error = ref('')
const isDragOver = ref(false)

// fileinputquote
const fileInput = ref(null)

// calculateattribute: is else can to submit
const canSubmit = computed(() => {
  return formData.value.simulationRequirement.trim() !== '' && files.value.length > 0
})

// triggerfileselect
const triggerFileInput = () => {
  if (!loading.value) {
    fileInput.value?.click()
  }
}

// processfileselect
const handleFileSelect = (event) => {
  const selectedFiles = Array.from(event.target.files)
  addFiles(selectedFiles)
}

// processdragrelated
const handleDragOver = (e) => {
  if (!loading.value) {
    isDragOver.value = true
  }
}

const handleDragLeave = (e) => {
  isDragOver.value = false
}

const handleDrop = (e) => {
  isDragOver.value = false
  if (loading.value) return
  
  const droppedFiles = Array.from(e.dataTransfer.files)
  addFiles(droppedFiles)
}

// addfile
const addFiles = (newFiles) => {
  const validFiles = newFiles.filter(file => {
    const ext = file.name.split('.').pop().toLowerCase()
    return ['pdf', 'md', 'txt'].includes(ext)
  })
  files.value.push(...validFiles)
}

// removefile
const removeFile = (index) => {
  files.value.splice(index, 1)
}

// scroll to footer
const scrollToBottom = () => {
  window.scrollTo({
    top: document.body.scrollHeight,
    behavior: 'smooth'
  })
}

// startsimulation - immediately jump turn , APIcall in Processpage enter line
const startSimulation = () => {
  if (!canSubmit.value || loading.value) return
  
  // storageuploaddata
  import('../store/pendingUpload.js').then(({ setPendingUpload }) => {
    setPendingUpload(files.value, formData.value.simulationRequirement)
    
    // immediately jump turn to Processpage( using special mark recognize table create newproject)
    router.push({
      name: 'Process',
      params: { projectId: 'new' }
    })
  })
}
</script>

